package hannah.yoo.itourette;


public class User{

    public String email;
    public String password;
    public String username;
    public String uid;

    public User() {
    }

    public User(String email, String password, String username, String uid) {
        this.email = email;
        this.password = password;
        this.username = username;
        this.uid = uid;
    }

}
